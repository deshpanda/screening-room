#!/usr/bin/env node
// Generates assets/worldmap.js — an equirectangular SVG path per country —
// from a Natural Earth-derived GeoJSON (public domain). One-off tool; rerun
// only if the map asset ever needs regenerating:
//   node tools/make-worldmap.mjs <countries.geo.json>
import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const src = process.argv[2];
if (!src) { console.error('usage: node tools/make-worldmap.mjs <countries.geo.json>'); process.exit(1); }

// Projection: plain equirectangular, longitude −180…180 → x 0…1000,
// latitude 85N…60S → y 0…403 (crops empty poles/Antarctica margin).
const W = 1000;
const TOP = 85, BOT = -60;
const H = Math.round((W / 360) * (TOP - BOT));
const px = (lon) => Math.round(((lon + 180) / 360) * W);
const py = (lat) => Math.round(((TOP - lat) / (TOP - BOT)) * H);

const geo = JSON.parse(readFileSync(src, 'utf8'));
const paths = {};
let dropped = 0;

for (const f of geo.features) {
  const name = f.properties.name;
  if (name === 'Antarctica') continue;
  const polys = f.geometry.type === 'Polygon' ? [f.geometry.coordinates] : f.geometry.coordinates;
  let d = '';
  for (const poly of polys) {
    for (const ring of poly) {
      const pts = [];
      for (const [lon, lat] of ring) {
        const x = px(lon), y = py(lat);
        const last = pts[pts.length - 1];
        if (!last || last[0] !== x || last[1] !== y) pts.push([x, y]);
      }
      if (pts.length < 4) { dropped++; continue; } // sub-pixel islets
      d += `M${pts.map((p) => p.join(' ')).join('L')}Z`;
    }
  }
  if (d) paths[name] = d;
}

const out = `// Generated by tools/make-worldmap.mjs from Natural Earth data (public domain).
// Equirectangular, viewBox 0 0 ${W} ${H}. Do not edit by hand.
export const WORLD_VIEW = [${W}, ${H}];
export const WORLD = ${JSON.stringify(paths)};
`;
writeFileSync(join(ROOT, 'assets', 'worldmap.js'), out);
console.log(`assets/worldmap.js — ${Object.keys(paths).length} countries, ${Math.round(out.length / 1024)} KB (${dropped} sub-pixel islets dropped)`);
